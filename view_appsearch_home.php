<h2>PHG アフィリエイト対応</h2>
<ul>
<li>自動生成される iTunes へのリンクをアフィリエイトリンクにする場合は、<a href="https://itunes.phgconsole.performancehorizon.com/login/itunes/jp?account%2Fsignout=" target="_blank">iTunes アフィリエイトプログラム</a> への申し込みが必要です。</li><br />
<li>
生成されるリンクにアフィリエイトトークン（アフィリエイト ID）を埋め込む場合には検索バー下の「アフィリエイトトークン」を入力した後に、アプリ検索をしてください。</li><br />

<li><a href="https://autolinkmaker.itunes.apple.com/?uo=8" target="_blank">Auto Link Maker</a> にてアフィリリンクにする場合は「アフィリエイトトークン」は空欄のまま、<a href="https://autolinkmaker.itunes.apple.com/?uo=8" target="_blank">Auto Link Maker</a> の Javascript ご自身の WEB サイトへ設置してください。
</li>

</ul><br />

